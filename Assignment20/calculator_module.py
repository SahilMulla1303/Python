
def add(a,b):
    add = a + b
    return add
    
def sub(a,b):
    sub = a - b
    return sub

def mul(a,b):
    mul = a * b
    return mul

def div(a,b):
    div = a / b
    return div

def mod(a,b):
    mod = a % b
    return mod

def fDiv(a,b):
    fDiv = a // b
    return fDiv

def power(a,b):
    result = a ** b
    return result

